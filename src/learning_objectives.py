def generate_learning_objectives(topic):

    topic = topic.lower()

    if "photosynthesis" in topic:

        return [
            "Understand what photosynthesis is",
            "Learn the role of sunlight",
            "Understand chlorophyll",
            "Know the inputs and outputs",
            "Explain why photosynthesis is important"
        ]

    return [
        "Understand the topic",
        "Learn key concepts",
        "Identify important terms",
        "Apply the concept",
        "Answer basic questions"
    ]